# Kapitel 4 - Villkor, IF-satser
# Programmering 1 med Python - Lärobok

# if tillsammans med else

v = 7

# med två if-satser
if v < 9:
    print('v är mindre än 9')
if v >= 9:
    print('v är större än eller lika med 9')

# blir mer läsbart med if - else
if v < 9:
    print('v är mindre än 9')
else:
    print('v är större än eller lika med 9')


